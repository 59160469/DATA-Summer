
public class Flower {
	String name;
	int prine;
	float f;
	
	public Flower() {
		
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getPrine() {
		return prine;
	}
	public void setPrine(int prine) {
		this.prine = prine;
	}
	public float getF() {
		return f;
	}
	public void setF(float f) {
		this.f = f;
	}
	
}
